// Automated check for the API-docs generator. The project has no JS unit runner
// (and we deliberately do not add one), so this is the RED→GREEN test for
// renderMarkdown: it asserts the output is idempotent and covers the contract
// (base URL, the filter vocabulary + modifiers, recipes, and key endpoints).
//
//   node scripts/gen-api-docs-smoke.mjs   # asserts; exits non-zero on failure

import { loadDocsModules, renderMarkdown } from './gen-api-docs.mjs';

const checks = [];
function assert(name, cond) {
  checks.push({ name, ok: Boolean(cond) });
}

async function main() {
  const { spec, filters } = await loadDocsModules();

  // Idempotency: same input → byte-identical output.
  const a = renderMarkdown(spec, filters);
  const b = renderMarkdown(spec, filters);
  assert('idempotent (two renders byte-identical)', a === b);

  // Generated header so no one hand-edits the file.
  assert('has generated header', a.includes('GENERATED by web/scripts/gen-api-docs.mjs'));
  assert('does not start with blank line', !a.startsWith('\n'));
  assert('ends with single newline', a.endsWith('\n') && !a.endsWith('\n\n'));

  // Base URL and core sections.
  assert('has base URL', a.includes('https://freehire.dev/api/v1'));
  assert('has filtering section', a.includes('## Filtering jobs'));
  assert('documents the search endpoint', a.includes('GET /jobs/search'));
  assert('documents the facets endpoint', a.includes('GET /jobs/facets'));

  // Filter vocabulary is present and derived (a known facet + a numeric/boolean one).
  assert('has a string facet (seniority)', a.includes('`seniority`'));
  assert('has the visa boolean filter', a.includes('`visa_sponsorship`'));
  assert('has the and-mode modifier', a.includes('_mode=and'));
  assert('has the exclude modifier', a.includes('_exclude'));

  // At least one worked recipe made it through.
  const recipe = filters.RECIPES[0];
  assert('has the first recipe query', a.includes(recipe.query));

  // Whole-API coverage spot-checks.
  assert('documents API keys', a.includes('/me/api-keys'));
  assert('documents saved searches', a.includes('/me/searches'));
  assert('documents auth', a.includes('/auth/login'));

  let failed = 0;
  for (const c of checks) {
    if (!c.ok) failed++;
    console.log(`${c.ok ? 'PASS' : 'FAIL'}  ${c.name}`);
  }
  if (failed) {
    console.error(`\n${failed} check(s) failed`);
    process.exit(1);
  }
  console.log(`\nAll ${checks.length} checks passed.`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
