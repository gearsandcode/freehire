// Generates docs/API.md from the single typed source web/src/lib/docs/api-spec.ts
// (+ filters.ts), so the repo Markdown and the /docs/api page never drift.
//
// Like scripts/og-smoke.mjs, it loads the TypeScript source through Vite's SSR
// loader (Vite is already a devDependency) — no .ts-extension imports, no new
// tooling. renderMarkdown is a PURE function (data in, string out) so the smoke
// (scripts/gen-api-docs-smoke.mjs) can assert its output without touching disk.
//
//   node scripts/gen-api-docs.mjs    # writes ../docs/API.md

import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { createServer } from 'vite';

const here = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(here, '..');
const outFile = resolve(webRoot, '..', 'docs', 'API.md');

const GENERATED_HEADER =
  '<!-- GENERATED by web/scripts/gen-api-docs.mjs from web/src/lib/docs/api-spec.ts. ' +
  'Do not edit by hand — run `pnpm run gen:api-docs`. -->';

function slug(s) {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

// Markdown table from a header row and string cells. Empty rows array yields "".
function table(headers, rows) {
  if (rows.length === 0) return '';
  const head = `| ${headers.join(' | ')} |`;
  const sep = `| ${headers.map(() => '---').join(' | ')} |`;
  const body = rows.map((r) => `| ${r.join(' | ')} |`).join('\n');
  return `${head}\n${sep}\n${body}`;
}

function paramRows(params) {
  return params.map((p) => [
    `\`${p.name}\``,
    p.type,
    p.required ? 'yes' : 'no',
    p.example ? `${p.description} (e.g. \`${p.example}\`)` : p.description,
  ]);
}

function renderEndpoint(ep, auths) {
  const out = [];
  out.push(`### \`${ep.method} ${ep.path}\``);
  out.push('');
  out.push(`**Auth:** ${auths[ep.auth]}`);
  out.push('');
  out.push(ep.summary);
  if (ep.description) {
    out.push('');
    out.push(ep.description);
  }
  if (ep.pathParams?.length) {
    out.push('');
    out.push('**Path parameters**');
    out.push('');
    out.push(table(['Name', 'Type', 'Required', 'Description'], paramRows(ep.pathParams)));
  }
  if (ep.query?.length) {
    out.push('');
    out.push('**Query parameters**');
    out.push('');
    out.push(table(['Name', 'Type', 'Required', 'Description'], paramRows(ep.query)));
  }
  if (ep.filterable) {
    out.push('');
    out.push('Plus every filter in [Filtering jobs](#filtering-jobs).');
  }
  if (ep.body?.length) {
    out.push('');
    out.push('**Body**');
    out.push('');
    out.push(table(['Name', 'Type', 'Required', 'Description'], paramRows(ep.body)));
  }
  out.push('');
  out.push('```bash');
  out.push(ep.curl);
  out.push('```');
  if (ep.responseExample) {
    out.push('');
    out.push('```json');
    out.push(ep.responseExample);
    out.push('```');
  }
  return out.join('\n');
}

function renderFilters(filters) {
  const { FILTER_FACETS, FILTER_EXTRAS, FILTER_MODIFIERS, RECIPES } = filters;
  const out = [];
  out.push('## Filtering jobs');
  out.push('');
  out.push(
    'These parameters apply to `GET /jobs/search` and `GET /jobs/facets`. Combine ' +
      'any of them with full-text `q`.',
  );
  out.push('');
  for (const m of FILTER_MODIFIERS) out.push(`- ${m}`);
  out.push('');
  out.push('### Facets');
  out.push('');
  out.push('Every facet below supports repeat-OR, `_mode=and`, and `_exclude` as described above.');
  out.push('');
  out.push(
    table(
      ['Param', 'Filter', 'Values'],
      FILTER_FACETS.map((f) => [`\`${f.param}\``, f.label, f.values]),
    ),
  );
  out.push('');
  out.push('### Numeric & boolean filters');
  out.push('');
  out.push(
    table(
      ['Param', 'Filter', 'Values'],
      FILTER_EXTRAS.map((f) => [`\`${f.param}\``, f.label, f.values]),
    ),
  );
  out.push('');
  out.push('### Recipes');
  out.push('');
  for (const r of RECIPES) {
    out.push(`- **${r.title}** — \`${r.query}\``);
  }
  return out.join('\n');
}

// Pure: (spec, filters) -> Markdown string. Deterministic, no IO.
export function renderMarkdown(spec, filters) {
  const { BASE_URL, OVERVIEW, GROUPS, AUTH_LABELS } = spec;
  const out = [];
  out.push(GENERATED_HEADER);
  out.push('');
  out.push('# freehire API');
  out.push('');
  out.push(`Base URL: \`${BASE_URL}\``);
  out.push('');

  // Table of contents.
  out.push('## Contents');
  out.push('');
  for (const o of OVERVIEW) out.push(`- [${o.title}](#${slug(o.title)})`);
  out.push('- [Filtering jobs](#filtering-jobs)');
  for (const g of GROUPS) out.push(`- [${g.title}](#${slug(g.title)})`);
  out.push('');

  // Overview sections.
  for (const o of OVERVIEW) {
    out.push(`## ${o.title}`);
    out.push('');
    for (const p of o.paragraphs) {
      out.push(p);
      out.push('');
    }
    if (o.code) {
      out.push('```json');
      out.push(o.code);
      out.push('```');
      out.push('');
    }
  }

  // Filters.
  out.push(renderFilters(filters));
  out.push('');

  // Endpoint groups.
  for (const g of GROUPS) {
    out.push(`## ${g.title}`);
    out.push('');
    out.push(g.intro);
    out.push('');
    for (const ep of g.endpoints) {
      out.push(renderEndpoint(ep, AUTH_LABELS));
      out.push('');
    }
  }

  return `${out.join('\n').trimEnd()}\n`;
}

// Loads the spec + filters modules through Vite's SSR loader.
export async function loadDocsModules() {
  const vite = await createServer({
    configFile: false,
    root: webRoot,
    logLevel: 'error',
    resolve: { alias: { $lib: resolve(webRoot, 'src/lib') } },
  });
  try {
    const spec = await vite.ssrLoadModule('/src/lib/docs/api-spec.ts');
    const filters = await vite.ssrLoadModule('/src/lib/docs/filters.ts');
    return { spec, filters };
  } finally {
    await vite.close();
  }
}

async function main() {
  const { spec, filters } = await loadDocsModules();
  const md = renderMarkdown(spec, filters);
  await writeFile(outFile, md);
  console.log(`Wrote ${outFile} (${md.length} bytes)`);
}

// Run only when invoked directly, so the smoke can import renderMarkdown/loadDocsModules.
if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main().catch((err) => {
    console.error(err);
    process.exit(1);
  });
}
